function editarColetor(nome, sobrenome, cpf) {
    
    let nomeColetor = document.getElementById('nomeColetor');
    let sobrenomeColetor = document.getElementById('sobrenomeColetor');
    let cpfColetor = document.getElementById('cpfColetor');

    nomeColetor.value = nome;
    sobrenomeColetor.value = sobrenome;
    cpfColetor.value = cpf;

}


